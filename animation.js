document.getElementById("on-btn").onclick = function() {
    document.getElementById("on-btn").classList.add('on-checked');
    document.getElementById("on-btn").classList.remove('on-not-checked');
    document.getElementById("off-btn").classList.add('off-not-checked');
    document.getElementById("off-btn").classList.remove('off-checked');
    document.querySelectorAll(".radio-img").forEach(radioimg => {
        radioimg.style.display="flex";
    });
    document.querySelectorAll(".additional-img").forEach(additionalimg => {
        additionalimg.style.display="block";
    });
}

document.getElementById("off-btn").onclick = function() {
    document.getElementById("off-btn").classList.add('off-checked');
    document.getElementById("off-btn").classList.remove('off-not-checked');
    document.getElementById("on-btn").classList.add('on-not-checked');
    document.getElementById("on-btn").classList.remove('on-checked');
    document.querySelectorAll(".radio-img").forEach(radioimg => {
        radioimg.style.display="none";
    });
    document.querySelectorAll(".additional-img").forEach(additionalimg => {
        additionalimg.style.display="none";
    });
}
